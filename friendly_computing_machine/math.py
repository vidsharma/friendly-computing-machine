"""
A small set of functions for doing math operations.
"""

#Write a function named add that adds two values
def add(arg1, arg2):
    """
    Function that adds two arguments
    """
    return  arg1 + arg2

def mult(arg1, arg2):
    """
    Function that multiplies two arguments"
    """
    return arg1 * arg2

def sub(arg1, arg2):
    """
    Function that subtracts arg2 from arg1
    """
    return arg1 - arg2
